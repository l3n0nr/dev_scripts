package Calculos;

/**
 *
 * @author lenonr
 */
public class Calculadora 
{
    public float soma (float x, float y)
    {
        return x+y;
    }
    
    public float subtracao(float x, float y)
    {
        return x-y;
    }
    
    public float multiplicacao(float x, float y)
    {
        return x*y;
    }
    
    public float divisao(float x, float y)
    {
        return x/y;
    }
}
