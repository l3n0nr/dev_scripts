# Mapa completo (ida)

$ zzrot47 '!"'"#$%&'()*+,-./"		#→ PQRSTUVWXYZ[\]^
$ zzrot47 0123456789			#→ _`abcdefgh
$ zzrot47 ':;<=>?@'			#→ ijklmno
$ zzrot47 ABCDEFGHIJKLMNOPQRSTUVWXYZ	#→ pqrstuvwxyz{|}~!"#$%&'()*+
$ zzrot47 '[\]^_`'			#→ ,-./01
$ zzrot47 abcdefghijklmnopqrstuvwxyz	#→ 23456789:;<=>?@ABCDEFGHIJK
$ zzrot47 '{|}~'			#→ LMNO

# Mapa completo (volta)

$ zzrot47 'PQRSTUVWXYZ[\]^'		#→ !"#$%&'()*+,-./
$ zzrot47 '_`abcdefgh'			#→ 0123456789
$ zzrot47 ijklmno			#→ :;<=>?@
$ zzrot47 "pqrstuvwxyz{|}~!\"#$%&'()*+"	#→ ABCDEFGHIJKLMNOPQRSTUVWXYZ
$ zzrot47 ,-./01			#→ [\]^_`
$ zzrot47 '23456789:;<=>?@ABCDEFGHIJK'	#→ abcdefghijklmnopqrstuvwxyz
$ zzrot47 LMNO				#→ {|}~

# Letras acentuadas não são alteradas

$ zzrot47 AÁaá				#→ pÁ2á
