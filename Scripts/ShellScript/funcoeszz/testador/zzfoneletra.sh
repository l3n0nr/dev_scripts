# Exemplos do --help
$ zzfoneletra 2345-LINUX	#→ 2345-54689
$ zzfoneletra 5555-HELP		#→ 5555-4357

# Mapa completo (e ignore case)
$ zzfoneletra ABCDEFGHIJKLMNOPQRSTUVWXYZ  #→ 22233344455566677778889999
$ zzfoneletra abcdefghijklmnopqrstuvwxyz  #→ 22233344455566677778889999

# E os acentos?
$ zzfoneletra AÁaá		#→ 2Á2Á
